
package projectoop;


public class Instructor {
    private String IName;

    // Default constructor
    public Instructor() {
    }

    // Constructor with instructor's name
    public Instructor(String IName) {
        this.IName = IName;
    }

    // Getter for IName
    public String getIName() {
        return IName;
    }

    // Setter for IName
    public void setIName(String IName) {

        this.IName = IName;
    
  }
}
    

